package com.example.responsi.Model;

public class Martabak {
    private String rating;
    private String martabak_name;
    private String price;
    private int image;

    public Martabak() {
        this.rating = rating;
        this.martabak_name = martabak_name;
        this.price = price;
        this.image = image;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getMartabak_name() {
        return martabak_name;
    }

    public void setMartabak_name(String martabak_name) {
        this.martabak_name = martabak_name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}

